package com.google.gwt.dev.json;

/**
 * Simple pair tuple.
 *
 * @param <A>
 * @param <B>
 */
public class Pair<A, B> {
  private final A a;
  private final B b;

  public Pair(A a, B b) {
    this.a = a;
    this.b = b;
  }

  public A getA() {
    return a;
  }

  public B getB() {
    return b;
  }
}
